class calendar {

  enum CalProvider_t {UNKNOWN, APPLE, GOOGLE};
  
  typedef struct {
    String owner; 
    String summary;
    time_t start;
    time_t end;
  } appointment_t;

  typedef struct {
    bool enabled = false;
    CalProvider_t type;
    String host = "";
    uint16_t port = 443;
    String path = "";
    String url = "";
    String owner = "";
    unsigned long lastupdate = 0;
  } url_t;
  
  public:
    calendar();
    void      StoreJsonConfig(String* json); 
    void      LoadJsonConfig();
    void      GetWebContent(ESPWebServer* server);
    
    void      addCalendar(url_t u);
    void      updateData(bool force);

    const uint8_t&  GetDayPeriod()    const {return DayPeriod;}
    void      SetDayPeriod(uint8_t days);
    
  private:
    std::vector<appointment_t> *appointment = NULL;
    std::vector<url_t> *CalURL = NULL;
    uint8_t DayPeriod; // Anzahl Tage die auf dem Display angezeigt werden sollen
    int     UpdateInterval;   //Anzahl Sekunden die die Calenderobjekte aktualisiert werden sollen

    WiFiClientSecure client;

    void    setRootCA(url_t* t);
    void    updateData(url_t *Cal);
    bool    testConnection(url_t* t);
    int     splitString(std::vector<String>* v, String* s);
    void    processCalendarObject(String* cal);
    time_t  iCalDate2Time(String* t);
    time_t  mkTime(String Date, String Time);
};

